// nullTerminatedString extracts a null-terminated string from a byte slice
func nullTerminatedString(b []byte) string {
	n := bytes.IndexByte(b, 0)
	if n == -1 {
		n = len(b)
	}
	return string(b[:n])
}

// int8ToString converts []int8 to string (C char arrays become []int8 in Go)
func int8ToString(arr []int8) string {
	b := make([]byte, len(arr))
	for i, v := range arr {
		b[i] = byte(v)
	}
	return nullTerminatedString(b)
}
